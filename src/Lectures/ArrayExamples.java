package Lectures;

public class ArrayExamples {
    public static void main(String[] args) {
        //Declare Array that will hold String types variables
        String [] names = new String [3];

        //How to set array's element value
        names[0] = "Anna";
        names[1] = "Anna2";
        names[2] = "Anna3";

        //How to access the value
        System.out.println(names[2]);
    }
}
